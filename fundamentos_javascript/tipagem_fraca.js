let variavel = 'Há uma string nesta variável'

// O fato de JS possuir uma tipagem fraca, não significa que os dados não possuem tipo.
// Significa apenas que a tipagem é dinâmica.
console.log(typeof variavel)

// Atribuindo outro tipo de dado a mesma variável, o seu tipo mudará
variavel = 3.14

console.log(typeof variavel)

//Tipo booleano
variavel = true

console.log(typeof variavel)

